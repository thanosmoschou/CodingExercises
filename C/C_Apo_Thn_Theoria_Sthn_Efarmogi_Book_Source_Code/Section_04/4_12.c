#include <stdio.h>
int main(void)
{
	unsigned char num, tmp;

	printf("Enter number [0-255]: ");
	scanf("%d", &num);

	tmp = num & 0xF; /* Στη μεταβλητή tmp αποθηκεύεται η τιμή των τεσσάρων χαμηλότερων bits του ακεραίου. Το 0xF αντιστοιχεί στο δυαδικό 1111. */
	tmp <<= 4; /*Ολίσθηση της τιμής τέσσερις θέσεις αριστερά*/
	tmp += num >> 4; /* Ολίσθηση του ακεραίου τέσσερις θέσεις δεξιά και πρόσθεση αυτής της τιμής στην tmp. */
	printf("%d\n", tmp);
	return 0;
}