public class Subscriber 
{
	private String name;
	private double hours;
	
	
	public Subscriber(String name, double hours) 
	{
		this.name = name;
		this.hours = hours;
	}

	
	public double calculateNetEnergy()
	{
		return hours;
	}
	
	
	public String getName()
	{
		return name;
	}


}
