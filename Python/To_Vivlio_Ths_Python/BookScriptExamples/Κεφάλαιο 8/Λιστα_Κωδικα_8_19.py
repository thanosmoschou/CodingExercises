#ΛΙΣΤΑ ΚΩΔΙΚΑ 8-19. Διαφορά – πληρώματα 
a=('Γιώργος','Νίκη','Ακης','Αννα')
b=('Αννα','Ακης','Βαγγέλης','Εύα')
c=('Αννα','Εύα','Βαγγέλης','Θανάσης')
a,b,c=set(a),set(b),set(c)
ap1=a-b-c
ap2=a&b&c
ap3=((a&b)|(b&c)|(a&c))-(a&b&c)
ap4=(a-b-c)|(b-a-c)|(c-b-a)
print(ap1)
print(ap2)
print(ap3)
print(ap4)
