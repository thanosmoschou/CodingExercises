try:
    x=float(input('Δώσε έναν αριθμό:'))
    y=1/x
    print(y)
except ValueError:
    print('Δεν έδωσες αριθμό. Ξαναπροσπάθησε...')
except ZeroDivisionError:
    print('Έδωσες μηδέν!Ο υπολογισμός δεν μπορεί να γίνει')
print('Τέλος προγράμματος')    
