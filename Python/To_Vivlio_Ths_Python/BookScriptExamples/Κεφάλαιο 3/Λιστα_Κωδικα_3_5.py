#ΛΙΣΤΑ ΚΩΔΙΚΑ 3-5. Απλή if – υπολογισμός έκπτωσης 
poso=float(input('Δώσε το ποσο αγοράς:'))
ekp=0
if poso>200:
    ekp=poso*10/100
    poso-=ekp
print('Η έκπτωση είναι:{0:6.2f}€ και το ποσό:{1:6.2f}€'.    format(ekp,poso))
