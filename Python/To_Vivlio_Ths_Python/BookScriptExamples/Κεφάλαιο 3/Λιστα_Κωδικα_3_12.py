#ΛΙΣΤΑ ΚΩΔΙΚΑ 3-12. Πολλαπλή if – χαρακτηρισμός βαθμού 
b=float(input('Δώσε τον Βαθμό:')) 
if 0<=b<5:      #b>=0 and b <5
    print('Ανεπαρκής')
elif 5<=b<7:    #b>=5 and b <7:
    print('Καλός')
elif 7<=b<8.5:  #b>=7 and b <8.5:
    print('Πολύ καλός')
else:#elif 8.5<=b<=10:#b>=8.5 and b <10:
    print('Αριστος')
