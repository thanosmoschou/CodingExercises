#ΛΙΣΤΑ ΚΩΔΙΚΑ 3-13. Πολλαπλή if – χαρακτηρισμός βαθμού - απλοί έλεγχοι 
b=float(input('Δώσε τον Βαθμό:')) 
if b<0 or b>10:
    print('Λάθος Δεδομένα.')
elif b<5:
    print('Ανεπαρκης')
elif b<7:
    print('Καλός')
elif b<8.5:  
    print('Πολύ καλός')
else:
    print('Αριστος')
