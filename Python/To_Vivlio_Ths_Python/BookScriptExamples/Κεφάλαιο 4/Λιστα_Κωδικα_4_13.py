#ΛΙΣΤΑ ΚΩΔΙΚΑ 4-13. while – τετράγωνα αριθμών 
counter=0
x=int(input('Δώσε έναν αριθμό:'))
while x>0:
    counter+=1
    print(x**2)
    x=int(input('Δώσε έναν αριθμό:'))
print('Δόθηκαν',counter,'αριθμοί')    
