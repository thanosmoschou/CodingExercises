#ΛΙΣΤΑ ΚΩΔΙΚΑ 4-4. for – στοιχεία φοιτητών – χρήση της zip 
names=['Ευα','Εφη','Γιώργος','Πέτρος']
grades=[8,6,9,5]
for name,grade in zip(names,grades):
    print('Ονομα φοιτητή:',name,'Βαθμός:',grade)
print('Τέλος προγράμματος')
