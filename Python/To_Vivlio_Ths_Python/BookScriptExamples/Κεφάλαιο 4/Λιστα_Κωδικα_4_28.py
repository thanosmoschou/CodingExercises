#ΛΙΣΤΑ ΚΩΔΙΚΑ 4-28. Πρώτος αριθμός με flag 
n=int(input('Δώσε έναν ακέραιο:'))
j=2
prime=True
while j<n:      
    if n%j==0:
        prime=False
        break
    j+=1
if prime:
    print('Το',n,'είναι πρώτος αριθμός')
else:
    print('Το',n,'δεν είναι πρώτος αριθμός')        
