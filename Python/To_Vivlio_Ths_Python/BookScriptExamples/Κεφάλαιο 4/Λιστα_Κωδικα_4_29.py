#ΛΙΣΤΑ ΚΩΔΙΚΑ 4-29. Πρώτος αριθμός με else 
n=int(input('Δώσε έναν ακέραιο:'))
j=2
while j<n:
    if not n%j:
        print('Το',n,'δεν είναι πρώτος αριθμός')
        break
    j+=1
else:
    print('Το',n,'είναι πρώτος αριθμός')

