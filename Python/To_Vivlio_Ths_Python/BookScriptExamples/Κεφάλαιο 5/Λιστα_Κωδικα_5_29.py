#ΛΙΣΤΑ ΚΩΔΙΚΑ 5-29. Χρήση της count – καταμέτρηση χαρακτήρων 
phrase=input('Δώσε μια φράση:')
letter=input('Δώσε έναν χαρακτήρα:')
phrase=list(phrase)
p=phrase.count(letter)
print(f'Ο χαρακτήρας {letter} εμφανίζεται {p} φορές')
