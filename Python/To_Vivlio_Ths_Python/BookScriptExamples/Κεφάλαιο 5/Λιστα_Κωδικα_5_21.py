#ΛΙΣΤΑ ΚΩΔΙΚΑ 5-21. Ταξινόμηση λίστας με τη χρήση συνάρτησης lambda 
grades=[['Σάββας',4],['Εφη',7],['Σήφης',6],['Λία',9], ['Πέτρος',8]]
grades.sort(key=lambda grade:grade[1], reverse=True)
print(grades)
