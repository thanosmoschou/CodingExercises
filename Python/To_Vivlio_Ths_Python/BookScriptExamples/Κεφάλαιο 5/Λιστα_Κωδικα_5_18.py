#ΛΙΣΤΑ ΚΩΔΙΚΑ 5-18. Ταξινόμηση λίστας με τη χρήση συνάρτησης len 
order1=['Καφές','Καλαμάκια']
order2=['Φίλτρα','Ποτήρια','Κουταλάκια','Αναδευτήρες']
order3=['Καφές','Ζάχαρη','Γάλα']
order4=['Καπάκια','Τσάι']
orders=[order1,order2,order3,order4]
orders.sort(key=len,reverse=True)
print(orders)
