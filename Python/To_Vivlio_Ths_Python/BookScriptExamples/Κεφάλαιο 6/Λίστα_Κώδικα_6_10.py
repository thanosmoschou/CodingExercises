acc=['έ',['ί','ϊ','ΐ'],['ϋ','ύ'],'ή']#Μερικά  μόνο από τα φω   νήεντα
noacc=['ε','ι','υ','η']  
s='έεόΰηήϊιέίϊΐυϋύή'  
for aa,i in enumerate(acc):  
    for _,j in enumerate(i): 
        s=s.replace(j, noacc[aa])  
