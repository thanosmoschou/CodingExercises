#ΛΙΣΤΑ ΚΩΔΙΚΑ 10-48. Άθροισμα αριθμών με αναδρομή 
n=15
def sumn(n):
    if n==0:
        return 0
    else:
        return sumn(n-1)+n
print(sumn(n))
